package projetosJava1;

public class Exercicio_EstruturasCondicionais {
    public static void main(String[] args) {
        int idade = 17;
        boolean isMaioDeIdade = idade >= 18;

        if (isMaioDeIdade) {
            System.out.println("Maior de idade");
        } else {
            System.out.println("Menor de idade");
        }

    }
}
