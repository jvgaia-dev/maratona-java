package Aulas;

public class AulaEstruturasCondicionais {
    public static void main(String[] args) {
        int idade = 17;
        boolean isMaiorDeIdade = idade >= 18;

        if (isMaiorDeIdade) {
            System.out.println("Maior de idade");
        } else {
            System.out.println("Menor de idade");
        }

    }
}
